		<h2>Agilumens</h2>
		Un cabinet de conseil en entreprise qui associe les compétences de consultants expérimentés. 
		L'originalité de sa démarche est de garantir le retour sur investissement en proposant une rémunération indexée sur les résultats mesurés.

