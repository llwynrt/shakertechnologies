<h1>On soutient</h1>

		<h2><a href="http://www.cousteau.org/fr/">Cousteau</a></h2>
		Parce qu'on a tous rêvé enfants devant ses documentaires.

		<h2><a href="https://www.lpo.fr/refuges-lpo/refuges-lpo">LPO</a></h2>
		Créer un refuge pour oiseaux sur votre balcon ou dans un jardin, même petit, même en entreprise, c'est possible et c'est utile !

		<h2><a href="http://www.conserv-action.com/">conserv-action</a></h2>
		Une association angevine agréée pour le 1% pour la planète. Humble et très concret.

		<h2><a href="http://www.lamersalee.com/">lamersalee</a></h2>
		éditeur local de livres engagés.

		<h2><a href="http://kiva.org/">kiva</a></h2>
		Une organisation à but non lucratif qui a pour mission de connecter les gens pour lutter contre la pauvreté. 

                <h2><a href="http://girlzinweb.com/">Girlz in web</a></h2>
                Association française dont l’objectif principal est la promotion et la valorisation des femmes dans le numérique et les nouvelles technologies.

                <h2><a href="http://emmabuntus.sourceforge.net/">Emmabuntüs</a></h2>
                Emmabuntüs est une distribution Linux dérivée d'Ubuntu construite pour reconditionner des ordinateurs donnés aux Communautés Emmaüs.
                
                <h2><a href="http://youandjerrycan.org/">Jerry DIT</a></h2>
                Jerry Do-It-Together est un ordinateur PC fabriqué avec des composants informatiques de réemploi assemblés dans un bidon de vingt litres.
                En Afrique, Jerry est devenu un outil de Réemploi pour les déchets électroniques.
                
                <h2><a href="http://www.greenlabcenter.com/">Green Lab Center</a></h2>
                Le Green Lab Center est un cluster unique ayant pour vocation la promotion de l'innovation et du savoir-faire Green IT.
                